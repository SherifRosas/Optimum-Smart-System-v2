@pytest.fixture
def authenticated_client(api_client, user):
    """Return an authenticated API client"""
    refresh = RefreshToken.for_user(user)
    api_client.credentials(HTTP_AUTHORIZATION=f'Bearer {refresh.access_token}')
    return api_client


@pytest.fixture
def supplier():
    return Supplier.objects.create(
        name='Test Supplier',
        email='supplier@example.com',
        phone_number='0987654321',
        is_active=True
    )


@pytest.mark.django_db
class TestSupplierViewSet:
    def test_list_suppliers(self, authenticated_client, supplier):
        """Test listing suppliers"""
        url = reverse('supplier-list')
        response = authenticated_client.get(url)
        assert response.status_code == status.HTTP_200_OK
        assert len(response.data['results']) > 0

    def test_create_supplier(self, authenticated_client):
        """Test creating a new supplier"""
        url = reverse('supplier-list')
        data = {
            'name': 'New Supplier',
            'email': 'newsupplier@example.com',
            'phone_number': '1111111111',
            'contact_person': 'John Doe',
            'address': '123 Main St',
            'is_active': True
        }
        response = authenticated_client.post(url, data, format='json')
        assert response.status_code == status.HTTP_201_CREATED
        assert Supplier.objects.filter(name='New Supplier').exists()

    def test_retrieve_supplier(self, authenticated_client, supplier):
        """Test retrieving a single supplier"""
        url = reverse('supplier-detail', kwargs={'pk': supplier.pk})
        response = authenticated_client.get(url)
        assert response.status_code == status.HTTP_200_OK
        assert response.data['id'] == supplier.id

    def test_update_supplier(self, authenticated_client, supplier):
        """Test updating a supplier"""
        url = reverse('supplier-detail', kwargs={'pk': supplier.pk})
        data = {'name': 'Updated Supplier'}
        response = authenticated_client.patch(url, data, format='json')
        assert response.status_code == status.HTTP_200_OK
        supplier.refresh_from_db()
        assert supplier.name == 'Updated Supplier'

    def test_top_rated_suppliers(self, authenticated_client, supplier):
        """Test getting top-rated suppliers"""
        url = reverse('supplier-top-rated')
        response = authenticated_client.get(url)
        assert response.status_code == status.HTTP_200_OK
        assert isinstance(response.data, list)

    def test_search_suppliers(self, authenticated_client, supplier):
        """Test searching suppliers"""
        url = reverse('supplier-search')
        response = authenticated_client.get(url, {'q': 'Test'})
        assert response.status_code == status.HTTP_200_OK
        assert len(response.data) > 0

    def test_unauthenticated_access(self, api_client):
        """Test that unauthenticated requests are rejected"""
        url = reverse('supplier-list')
        response = api_client.get(url)
        assert response.status_code == status.HTTP_401_UNAUTHORIZED











































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































